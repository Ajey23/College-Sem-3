//MATH FUNCTION IN JAVA

class MathFun
{
    public static void main(String args[])
    {
        int n1=2, n2=3, n3=5;
        System.out.println("\n Max is: "+ Math.max(n1,n2));
        System.out.println("\n Power is: "+ Math.pow(n1,n2));
        System.out.println("\n Squreroot is: "+ Math.sqrt(n3));
    }
}